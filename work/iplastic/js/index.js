/*눈*/
$(function(){
   $('.bo_he01').on('mouseover',function(){
      $('.bo_he01 > em').css({
         'background-image':'url(img/sub_gnb_ic01_hover.PNG)' 
      }),
          $('.bo_he01 > p').css({
         'color':'#6397c7' 
      });
   }); 
});
$(function(){
   $('.bo_he01').on('mouseleave',function(){
      $('.bo_he01 > em').css({
         'background-image':'' 
      }),
          $('.bo_he01 > p').css({
         'color':'' 
      });
   }); 
});
